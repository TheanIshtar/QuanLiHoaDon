    public void chu_chay() {
        Thread threadl = new Thread() {
            @Override
            public void run() {
                String txt = " " + lblChuChay.getText() + " ";
                while (true) {
                    txt = txt.substring(1,txt.length() ) + txt.charAt(0);
                    try {
                        sleep(500);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Infor_Form.class.getName()).log(Level.SEVERE, null, ex);
                    }
                lblChuChay.setText(txt);
                }
            }
            
        };
        threadl.start();
    }
    
    public void TimeRun() {
        new Thread() {
            public void run() {
                while (timerun == 0) {
                    Calendar cal = new GregorianCalendar();
                    int second = cal.get(Calendar.SECOND);
                    int minute = cal.get(Calendar.MINUTE);
                    int hour = cal.get(Calendar.HOUR_OF_DAY);
                    int AM_PM = cal.get(Calendar.AM_PM);
                    String day_night = "";
                    if (AM_PM == 1) {
                        day_night = "PM";
                    } else {
                        day_night = "AM";
                    }
                    String str = hour + ":" + minute + ":" + second + " " + day_night;
                    lblTime.setText(str);
                }
            }
        }.start();

    }
    
